import random

quotes = [
    "Жизнь — это то, что происходит, пока мы строим планы.",
    "Будь тем изменением, которое хочешь увидеть в мире.",
    "Сложности делают нас сильнее.",
    "Лучший способ предсказать будущее — это создать его.",
    "Успех — это способность идти от неудачи к неудаче, не теряя энтузиазма."
]

def get_random_quote():
    return random.choice(quotes)

def main():
    print("Программа случайных цитат")
    print("==========================")
    print(get_random_quote())
    input("\nНажмите Enter, чтобы выйти...")

if __name__ == "__main__":
    main()